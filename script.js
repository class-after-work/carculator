const carculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  powerof: function (a, b) {
    console.log(a ** b);
  },
};

carculator.add(2, 4);
carculator.minus(5, 4);
carculator.divide(10, 2);
carculator.powerof(2, 2);
